/// A trajectory profile: an initial nav velocity and the continuous inputs
/// (desired nav acceleration, body angular rate) as a function of time and
/// current nav velocity. Shared by `mc_validation` and `integration_fidelity`.
pub struct Profile {
    pub name: &'static str,
    pub v0: Vector3<f64>,
    pub inputs: fn(f64, Vector3<f64>) -> (Vector3<f64>, Vector3<f64>),
}

/// Battery spanning benign-to-aggressive dynamics.
pub fn profiles() -> [Profile; 4] {
    [
        Profile {
            name: "static hover",
            v0: Vector3::zeros(),
            inputs: |_t, _v| (Vector3::zeros(), Vector3::zeros()),
        },
        Profile {
            name: "constant velocity",
            v0: Vector3::new(12.0, 0.0, 0.0),
            inputs: |_t, _v| (Vector3::zeros(), Vector3::zeros()),
        },
        Profile {
            name: "coordinated turn",
            v0: Vector3::new(10.0, 0.0, 0.0),
            inputs: |_t, v| {
                let w = Vector3::new(0.0, 0.0, 0.3); // 0.3 rad/s yaw, radius ~33 m
                (w.cross(&v), w) // centripetal accel keeps |v| constant
            },
        },
        Profile {
            name: "aggressive 3-D",
            v0: Vector3::new(15.0, 0.0, 0.0),
            inputs: |t, v| {
                // Coupled roll/pitch oscillation + steady fast yaw, plus a
                // vertical accel — large attitude increments per step.
                let w = Vector3::new(0.3 * (0.5 * t).sin(), 0.2 * (0.7 * t).cos(), 0.5);
                let a = w.cross(&v) + Vector3::new(0.0, 0.0, 2.0 * (0.8 * t).sin());
                (a, w)
            },
        },
    ]
}

/// High-fidelity reference advance of the *continuous* kinematics defined by
/// `inputs` over `dt`: classic RK4 on (ṗ = v, v̇ = a_nav(t, v)) plus the exact
/// exponential map on attitude (q ← q·Exp(ω·h)), over `M` fine substeps. The
/// kinematic reference leaves the biases untouched (those are the IMU's). This
/// is the "truth" that first-order Euler is measured against — generating truth
/// with a *different, higher-order* integrator is what lets `integration_fidelity`
/// see the integration error that `mc_validation` structurally cannot.
pub fn reference_step(
    s: &Ins,
    t: f64,
    dt: f64,
    inputs: fn(f64, Vector3<f64>) -> (Vector3<f64>, Vector3<f64>),
) -> Ins {
    reference_step_with_imu(s, t, dt, inputs).0
}

/// Like [`reference_step`], but also returns the *interval-averaged* specific
/// force and angular rate over `[t, t+dt]` — the delta-velocity / delta-angle a
/// real strapdown IMU integrates and reports, in contrast to a single
/// start-of-interval (zero-order-hold) sample. Feeding these to the rate-form
/// mechanization captures the within-step motion the ZOH sample misses.
pub fn reference_step_with_imu(
    s: &Ins,
    t: f64,
    dt: f64,
    inputs: fn(f64, Vector3<f64>) -> (Vector3<f64>, Vector3<f64>),
) -> (Ins, Vector3<f64>, Vector3<f64>) {
    const M: usize = 100;
    let h = dt / M as f64;
    let g = g_vec();
    let a_of = |tt: f64, vv: Vector3<f64>| inputs(tt, vv).0;
    let mut p = pos(s);
    let mut v = vel(s);
    let mut q = att(s).uq;
    let mut f_int = Vector3::zeros(); // ∫ specific force (body frame)
    let mut w_int = Vector3::zeros(); // ∫ angular rate
    for j in 0..M {
        let tj = t + j as f64 * h;
        let (a_nav, omega) = inputs(tj, v);
        // Accumulate the body-frame specific force and rate over the substep.
        f_int += q.inverse_transform_vector(&(a_nav - g)) * h;
        w_int += omega * h;
        // Attitude: exact exponential map with the substep-midpoint rate.
        let w_mid = inputs(tj + 0.5 * h, v).1;
        q = q * UnitQuaternion::from_scaled_axis(w_mid * h);
        // Translation: classic RK4 of ṗ = v, v̇ = a_nav(t, v).
        let k1v = a_of(tj, v);
        let k1p = v;
        let k2v = a_of(tj + 0.5 * h, v + k1v * (0.5 * h));
        let k2p = v + k1v * (0.5 * h);
        let k3v = a_of(tj + 0.5 * h, v + k2v * (0.5 * h));
        let k3p = v + k2v * (0.5 * h);
        let k4v = a_of(tj + h, v + k3v * h);
        let k4p = v + k3v * h;
        v += (k1v + k2v * 2.0 + k3v * 2.0 + k4v) * (h / 6.0);
        p += (k1p + k2p * 2.0 + k3p * 2.0 + k4p) * (h / 6.0);
    }
    (state(p, v, SO3 { uq: q }, bias_a(s), bias_g(s)), f_int / dt, w_int / dt)
}
